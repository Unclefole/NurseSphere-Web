// ─── Main Export ──────────────────────────────────────────────────────────────

/**
 * Generate an HTML contract document and upload to Supabase Storage.
 * Returns the public URL of the uploaded document.
 *
 * @param contractId  - UUID of the contract record
 * @param templateId  - Optional UUID of a contract_templates record
 * @param variables   - Variable substitutions for the template
 * @returns Public URL of the uploaded HTML document
 */
export async function generateContractPdf(
  contractId: string,
  templateId?: string | null,
  variables: ContractVariables = {}
): Promise<string> {
  const supabase = getServiceClient()

  let htmlBody: string
  let documentTitle = `Contract ${contractId.slice(0, 8).toUpperCase()}`

  if (templateId) {
    // Fetch template from DB
    const { data: template, error } = await supabase
      .from('contract_templates')
      .select('name, content')
      .eq('id', templateId)
      .single()

    if (error || !template) {
      throw new Error(`Template not found: ${templateId}`)
    }

    documentTitle = template.name
    htmlBody = interpolate(template.content, variables)
  } else {
    // Use built-in default template
    htmlBody = buildDefaultTemplate(variables)
  }

  const fullHtml = wrapHtml(htmlBody, documentTitle)
  const fileName = `contracts/${contractId}/document_${Date.now()}.html`

  // Ensure the storage bucket exists (phantom guard)
  const bucketName = process.env.SUPABASE_CONTRACTS_BUCKET ?? 'contracts'

  const { error: uploadError } = await supabase.storage
    .from(bucketName)
    .upload(fileName, Buffer.from(fullHtml, 'utf-8'), {
      contentType: 'text/html',
      upsert: true,
    })

  if (uploadError) {
    // Fallback: store HTML as base64 data URL if storage fails
    console.warn('[PDFGenerator] Storage upload failed, using data URL fallback:', uploadError.message)
    const base64 = Buffer.from(fullHtml, 'utf-8').toString('base64')
    return `data:text/html;base64,${base64}`
  }

  // Get public URL
  const { data: urlData } = supabase.storage
    .from(bucketName)
    .getPublicUrl(fileName)

  return urlData.publicUrl
}
