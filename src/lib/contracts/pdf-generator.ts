/**
 * Contract PDF Generator — V1 Stub
 *
 * Generates an HTML document from a contract template + variables,
 * uploads it to Supabase Storage, and returns the public URL.
 *
 * NOTE: In production, swap the HTML generation for a proper PDF library:
 *   - pdf-lib (pure JS, no headless browser needed)
 *   - puppeteer (headless Chrome, high fidelity)
 *   - WeasyPrint (server-side Python, excellent CSS support)
 *
 * Template variable syntax: {{variable_name}}
 * Supported variables:
 *   {{nurse_name}}, {{facility_name}}, {{shift_date}}, {{rate}},
 *   {{hours}}, {{role}}, {{start_date}}, {{end_date}},
 *   {{platform_fee}}, {{total_compensation}}
 */

import { createClient } from '@supabase/supabase-js'

// ─── Types ────────────────────────────────────────────────────────────────────

export interface ContractVariables {
  nurse_name?: string
  facility_name?: string
  shift_date?: string
  rate?: string | number
  hours?: string | number
  role?: string
  start_date?: string
  end_date?: string
  platform_fee?: string | number
  total_compensation?: string | number
  [key: string]: string | number | undefined
}

export interface GeneratePdfOptions {
  contractId: string
  templateId?: string
  variables?: ContractVariables
  /** Pre-rendered HTML content (skips template fetch if provided) */
  htmlContent?: string
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function getServiceClient() {
  const url = process.env.NEXT_PUBLIC_SUPABASE_URL
  const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY
  if (!url || !serviceKey) {
    throw new Error('Supabase env vars missing for PDF generator.')
  }
  return createClient(url, serviceKey, {
    auth: { autoRefreshToken: false, persistSession: false },
  })
}

/**
 * Replace {{variable}} placeholders in a template string.
 */
function interpolate(template: string, variables: ContractVariables): string {
  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => {
    const val = variables[key]
    return val !== undefined ? String(val) : `{{${key}}}`
  })
}

